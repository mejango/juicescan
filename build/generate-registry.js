#!/usr/bin/env node
/**
 * generate-registry.js — Reads ABIs from data/abis/, applies ordering heuristics,
 * and generates src/abi-registry.js as a single importable module.
 *
 * Usage:
 *   node build/generate-registry.js
 *
 * Output:
 *   src/abi-registry.js — ES module exporting { contracts, categories, manifest,
 *                          tokens, commonActions }
 */

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const ABI_DIR = path.join(ROOT, "data", "abis");
const DATA_DIR = path.join(ROOT, "data");
const OUT_FILE = path.join(ROOT, "src", "abi-registry.js");

// ── Load data files ──────────────────────────────────────────────────────────

function loadJSON(filename) {
  const filepath = path.join(DATA_DIR, filename);
  if (!fs.existsSync(filepath)) {
    console.error(`Missing data file: ${filepath}`);
    process.exit(1);
  }
  return JSON.parse(fs.readFileSync(filepath, "utf8"));
}

const manifest = loadJSON("manifest.json");
const categories = loadJSON("categories.json");
const tokens = loadJSON("tokens.json");
const commonActions = loadJSON("common-actions.json");
const functionOrder = loadJSON("function-order.json");
const formViews = loadJSON("form-views.json");
const deployments = loadJSON("deployments.json");

// Source bodies + GitHub links — optional, generated by extract-sources.js.
let contractSources = {};
try {
  contractSources = loadJSON("contract-sources.json");
} catch (e) {
  console.warn("contract-sources.json not found — run `node build/extract-sources.js` to populate source bodies and GitHub links.");
}

// ── Load ABIs ────────────────────────────────────────────────────────────────

function loadABIs() {
  const abis = {};
  const natspecRaw = {};
  const abiFiles = fs.readdirSync(ABI_DIR).filter((f) => f.endsWith(".json"));

  for (const file of abiFiles) {
    const name = file.replace(".json", "");
    const raw = JSON.parse(fs.readFileSync(path.join(ABI_DIR, file), "utf8"));

    // Support both old format (plain array) and new format ({ abi, devdoc, userdoc })
    if (Array.isArray(raw)) {
      abis[name] = raw;
      natspecRaw[name] = { devdoc: {}, userdoc: {} };
    } else {
      abis[name] = raw.abi || [];
      natspecRaw[name] = {
        devdoc: raw.devdoc || {},
        userdoc: raw.userdoc || {},
        contractNotice: raw.contractNotice || '',
        contractTitle: raw.contractTitle || '',
      };
    }
  }

  return { abis, natspecRaw };
}

// ── Build NatSpec map ────────────────────────────────────────────────────────

function buildFunctionSignature(abiEntry) {
  if (!abiEntry || abiEntry.type !== "function") return null;
  var inputs = (abiEntry.inputs || []).map(function (inp) {
    if (inp.type === "tuple") return buildTupleType(inp);
    if (inp.type === "tuple[]") return buildTupleType(inp) + "[]";
    return inp.type;
  });
  return abiEntry.name + "(" + inputs.join(",") + ")";
}

function buildTupleType(param) {
  var components = (param.components || []).map(function (c) {
    if (c.type === "tuple") return buildTupleType(c);
    if (c.type === "tuple[]") return buildTupleType(c) + "[]";
    return c.type;
  });
  return "(" + components.join(",") + ")";
}

function buildNatspec(abis, natspecRaw) {
  const natspec = {};

  for (const [contractName, abi] of Object.entries(abis)) {
    const raw = natspecRaw[contractName] || { devdoc: {}, userdoc: {} };
    const devdoc = raw.devdoc || {};
    const userdoc = raw.userdoc || {};
    const contractNatspec = {};

    // Build a map from signature -> natspec for each function in the ABI
    for (const entry of abi) {
      if (entry.type !== "function") continue;
      const sig = buildFunctionSignature(entry);
      if (!sig) continue;

      const dev = devdoc[sig] || {};
      const user = userdoc[sig] || {};

      // Only include if there is actual documentation
      if (!user.notice && !dev.details && !dev.params) continue;

      const fnDoc = {};
      if (user.notice) fnDoc.notice = user.notice;
      if (dev.details) fnDoc.details = dev.details;
      if (dev.params) fnDoc.params = dev.params;

      contractNatspec[entry.name] = fnDoc;
    }

    if (Object.keys(contractNatspec).length > 0) {
      natspec[contractName] = contractNatspec;
    }
  }

  return natspec;
}

// ── Priority heuristic ──────────────────────────────────────────────────────

const ACTION_NAMES = /^(pay|mint|burn|deploy|launch|queue|send|cashOut)/;
const CONFIG_NAMES = /^(set|add|remove)/;

function functionPriority(abiEntry) {
  if (!abiEntry || abiEntry.type !== "function") return 99;

  const name = abiEntry.name || "";
  const mutability = abiEntry.stateMutability || "";

  // Payable functions first
  if (mutability === "payable") return 0;

  // Action verbs
  if (ACTION_NAMES.test(name)) return 1;

  // Configuration setters
  if (CONFIG_NAMES.test(name)) return 2;

  // View/pure (read functions)
  if (mutability === "view" || mutability === "pure") return 3;

  // Everything else (nonpayable that didn't match above)
  return 4;
}

function sortABI(contractName, abi) {
  const overrides = functionOrder[contractName] || {};

  // Separate functions from events/errors/etc
  const functions = [];
  const nonFunctions = [];

  for (const entry of abi) {
    if (entry.type === "function") {
      functions.push(entry);
    } else {
      nonFunctions.push(entry);
    }
  }

  // Sort functions by priority
  functions.sort((a, b) => {
    const aOverride = overrides[a.name];
    const bOverride = overrides[b.name];

    // If both have overrides, sort by override value
    if (aOverride !== undefined && bOverride !== undefined) {
      return aOverride - bOverride;
    }

    // Overridden functions come first
    if (aOverride !== undefined) return -1;
    if (bOverride !== undefined) return 1;

    // Default: sort by priority heuristic, then alphabetical
    const aPri = functionPriority(a);
    const bPri = functionPriority(b);
    if (aPri !== bPri) return aPri - bPri;

    return (a.name || "").localeCompare(b.name || "");
  });

  // Return functions first, then events, errors, etc. (sorted by type then name)
  nonFunctions.sort((a, b) => {
    const typeOrder = { event: 0, error: 1, constructor: 2, fallback: 3, receive: 4 };
    const aType = typeOrder[a.type] ?? 5;
    const bType = typeOrder[b.type] ?? 5;
    if (aType !== bType) return aType - bType;
    return (a.name || "").localeCompare(b.name || "");
  });

  return [...functions, ...nonFunctions];
}

function buildCategories(rawCategories, contractNames) {
  const known = new Set(contractNames);
  const listed = new Set();
  const result = {};

  for (const [category, names] of Object.entries(rawCategories)) {
    const filtered = names.filter((name) => known.has(name));
    if (filtered.length === 0) continue;
    result[category] = filtered;
    for (const name of filtered) listed.add(name);
  }

  const unlisted = contractNames
    .filter((name) => !listed.has(name))
    .sort((a, b) => a.localeCompare(b));
  if (unlisted.length > 0) {
    result["Deployments"] = unlisted;
  }

  return result;
}

// ── Generate registry ───────────────────────────────────────────────────────

function generate() {
  const { abis, natspecRaw } = loadABIs();

  // Build sorted contracts map
  const contracts = {};
  for (const [name, abi] of Object.entries(abis)) {
    contracts[name] = sortABI(name, abi);
  }

  // Build NatSpec documentation map
  const natspec = buildNatspec(contracts, natspecRaw);

  // Validate common actions reference real functions
  for (const group of commonActions) {
    for (const action of group.entries) {
      const contractABI = contracts[action.contract];
      if (!contractABI) {
        console.warn(
          `WARNING: common-actions references unknown contract: ${action.contract}`
        );
        continue;
      }
      const found = contractABI.find(
        (e) => e.type === "function" && e.name === action.function
      );
      if (!found) {
        console.warn(
          `WARNING: common-actions references unknown function: ${action.contract}.${action.function}`
        );
      }
    }
  }

  // Build contract metadata (merge ABI with manifest info)
  const contractMeta = {};
  for (const [name, abi] of Object.entries(contracts)) {
    const info = manifest.contracts[name] || {};
    const deploymentInfo = deployments.deployments
      ? deployments.deployments[name] || null
      : null;
    const fnCount = abi.filter((e) => e.type === "function").length;
    const eventCount = abi.filter((e) => e.type === "event").length;
    const errorCount = abi.filter((e) => e.type === "error").length;

    const rawNs = natspecRaw[name] || {};
    const metaEntry = {
      singleton: info.singleton ?? true,
      addresses: info.addresses || {},
      contractName: info.contractName || (deploymentInfo && deploymentInfo.contractName) || name,
      deploymentName: info.deploymentName || name,
      stats: { functions: fnCount, events: eventCount, errors: errorCount },
    };
    if (deploymentInfo && deploymentInfo.sourceName) metaEntry.sourceName = deploymentInfo.sourceName;
    if (rawNs.contractNotice) metaEntry.notice = rawNs.contractNotice;
    if (rawNs.contractTitle) metaEntry.title = rawNs.contractTitle;
    contractMeta[name] = metaEntry;
  }

  const outputCategories = buildCategories(categories, Object.keys(contracts));

  // Determine which category each contract belongs to (reverse lookup)
  const contractToCategory = {};
  for (const [cat, names] of Object.entries(outputCategories)) {
    for (const name of names) {
      contractToCategory[name] = cat;
    }
  }

  // Build output
  const output = {
    contracts,        // name → sorted ABI array
    meta: contractMeta, // name → { singleton, addresses, stats }
    natspec,          // name → { fnName → { notice, details, params } }
    categories: outputCategories, // category → [contract names]
    contractToCategory, // contract name → category
    chains: manifest.chains,
    deployments,
    tokens,
    commonActions,
    formViews,        // contract → { fnName → { layout, steps, labels, ... } }
    sources: contractSources, // contract → { repo, githubUrl, branch, path, startLine, endLine, functionsByName: { name → [{ source, startLine, endLine, paramTypes }] } }
  };

  // Generate JS module
  const js = [
    "// AUTO-GENERATED by build/generate-registry.js — do not edit manually",
    `// Generated: ${new Date().toISOString()}`,
    `// Contracts: ${Object.keys(contracts).length}`,
    `// Total functions: ${Object.values(contracts).reduce((sum, abi) => sum + abi.filter((e) => e.type === "function").length, 0)}`,
    "",
    `export const registry = ${JSON.stringify(output, null, 2)};`,
    "",
    "// Convenience accessors",
    "export const contracts = registry.contracts;",
    "export const meta = registry.meta;",
    "export const natspec = registry.natspec;",
    "export const categories = registry.categories;",
    "export const contractToCategory = registry.contractToCategory;",
    "export const chains = registry.chains;",
    "export const deployments = registry.deployments;",
    "export const tokens = registry.tokens;",
    "export const commonActions = registry.commonActions;",
    "export const formViews = registry.formViews;",
    "export const sources = registry.sources;",
    "",
    "/**",
    " * Get the GitHub source info for a contract.",
    " * @param {string} name - Contract name",
    " * @returns {object|null} { repo, githubUrl, branch, path, startLine, endLine } or null",
    " */",
    "export function getContractSource(name) {",
    "  if (!registry.sources) return null;",
    "  if (registry.sources[name]) return registry.sources[name];",
    "  const m = registry.meta[name];",
    "  return m && m.contractName ? (registry.sources[m.contractName] || null) : null;",
    "}",
    "",
    "/**",
    " * Get the source body for a function. Resolves overloads by matching",
    " * ABI parameter types against parsed Solidity parameter types.",
    " * @param {string} contractName",
    " * @param {object} abiEntry - The ABI function entry",
    " * @returns {object|null} { source, startLine, endLine, paramTypes } or null",
    " */",
    "export function getFunctionSource(contractName, abiEntry) {",
    "  if (!registry.sources) return null;",
    "  const m = registry.meta[contractName];",
    "  const sourceName = m && m.contractName ? m.contractName : contractName;",
    "  const c = registry.sources[contractName] || registry.sources[sourceName];",
    "  if (!c || !c.functionsByName) return null;",
    "  const matches = c.functionsByName[abiEntry.name];",
    "  if (!matches || matches.length === 0) return null;",
    "  if (matches.length === 1) return matches[0];",
    "  // Overloaded: pick the one whose arity matches and whose param types best match.",
    "  const abiTypes = (abiEntry.inputs || []).map(function(p) { return p.type; });",
    "  const abiInternal = (abiEntry.inputs || []).map(function(p) {",
    "    var t = p.internalType || p.type;",
    "    return t.replace(/^struct\\s+/, '').replace(/^contract\\s+/, '').replace(/^enum\\s+/, '');",
    "  });",
    "  function score(srcParamTypes) {",
    "    if (srcParamTypes.length !== abiTypes.length) return -1;",
    "    var s = 0;",
    "    for (var i = 0; i < srcParamTypes.length; i++) {",
    "      var srcTok = srcParamTypes[i].replace(/\\s+/g, ' ').trim();",
    "      if (srcTok === abiTypes[i]) { s += 2; continue; }",
    "      // Try matching against internalType (for structs/contracts/enums)",
    "      if (srcTok === abiInternal[i]) { s += 2; continue; }",
    "      // Loose suffix match (handles 'IFoo' vs 'address', arrays)",
    "      if (srcTok.endsWith(']') && abiTypes[i].endsWith(']')) { s += 1; }",
    "    }",
    "    return s;",
    "  }",
    "  var best = matches[0];",
    "  var bestScore = score(best.paramTypes);",
    "  for (var i = 1; i < matches.length; i++) {",
    "    var s = score(matches[i].paramTypes);",
    "    if (s > bestScore) { best = matches[i]; bestScore = s; }",
    "  }",
    "  return best;",
    "}",
    "",
    "/**",
    " * Build a GitHub permalink for a contract or a function.",
    " * @param {string} contractName",
    " * @param {object} [abiEntry] - If provided, link to the function's line range",
    " * @returns {string|null}",
    " */",
    "export function getGithubUrl(contractName, abiEntry) {",
    "  const c = getContractSource(contractName);",
    "  if (!c || !c.githubUrl) return null;",
    "  const base = c.githubUrl + '/blob/' + (c.branch || 'main') + '/' + c.path;",
    "  if (!abiEntry) {",
    "    return base + '#L' + c.startLine + '-L' + c.endLine;",
    "  }",
    "  const fn = getFunctionSource(contractName, abiEntry);",
    "  if (!fn) return base;",
    "  return base + '#L' + fn.startLine + '-L' + fn.endLine;",
    "}",
    "",
    "/**",
    " * Get the sorted ABI for a contract by name.",
    " * @param {string} name - Contract name (e.g. 'JBMultiTerminal')",
    " * @returns {Array} ABI array, or undefined if not found",
    " */",
    "export function getABI(name) {",
    "  return registry.contracts[name];",
    "}",
    "",
    "/**",
    " * Get only the function entries from a contract's ABI.",
    " * @param {string} name - Contract name",
    " * @returns {Array} Function ABI entries, sorted by priority",
    " */",
    "export function getFunctions(name) {",
    "  const abi = registry.contracts[name];",
    '  return abi ? abi.filter(e => e.type === "function") : [];',
    "}",
    "",
    "/**",
    " * Get the deployed address for a contract on a given chain.",
    " * @param {string} name - Contract name",
    " * @param {string|number} chainId - Chain ID",
    " * @returns {string|null} Address or null if not deployed",
    " */",
    "export function getAddress(name, chainId) {",
    "  const m = registry.meta[name];",
    "  return m ? (m.addresses[String(chainId)] || null) : null;",
    "}",
    "",
    "/**",
    " * Get deployment metadata for a deployment name.",
    " * @param {string} name - Deployment name, e.g. 'JBCCIPSucker__BASE_SEP'",
    " * @param {string|number} [chainId] - Optional chain ID",
    " * @returns {object|null}",
    " */",
    "export function getDeployment(name, chainId) {",
    "  const d = registry.deployments && registry.deployments.deployments",
    "    ? registry.deployments.deployments[name]",
    "    : null;",
    "  if (!d) return null;",
    "  if (chainId == null) return d;",
    "  return d.chains ? (d.chains[String(chainId)] || null) : null;",
    "}",
    "",
    "/**",
    " * List all contracts in a given category.",
    " * @param {string} category - Category name (e.g. 'Core Protocol')",
    " * @returns {Array<string>} Contract names",
    " */",
    "export function getContractsByCategory(category) {",
    "  return registry.categories[category] || [];",
    "}",
    "",
  ];

  // Ensure output directory exists
  const outDir = path.dirname(OUT_FILE);
  if (!fs.existsSync(outDir)) {
    fs.mkdirSync(outDir, { recursive: true });
  }

  fs.writeFileSync(OUT_FILE, js.join("\n"), "utf8");

  // Summary
  const totalFns = Object.values(contracts).reduce(
    (sum, abi) => sum + abi.filter((e) => e.type === "function").length,
    0
  );
  const totalEvents = Object.values(contracts).reduce(
    (sum, abi) => sum + abi.filter((e) => e.type === "event").length,
    0
  );

  const totalNatspec = Object.values(natspec).reduce(
    (sum, c) => sum + Object.keys(c).length,
    0
  );

  console.log("generate-registry.js");
  console.log("─────────────────────");
  console.log(`Contracts: ${Object.keys(contracts).length}`);
  console.log(`Functions: ${totalFns}`);
  console.log(`Events:    ${totalEvents}`);
  console.log(`NatSpec:   ${totalNatspec} documented functions`);
  console.log(`Categories: ${Object.keys(outputCategories).length}`);
  console.log(`Output:    ${OUT_FILE}`);
  console.log(
    `Size:      ${(fs.statSync(OUT_FILE).size / 1024).toFixed(1)} KB`
  );
}

// ── Run ──────────────────────────────────────────────────────────────────────

generate();
